package Beans;

public class MainBorderBean {
	
	private int border_number;
	private String border_title;
	private String border_name;
	private String createdate;
	private String updatedate;
	
	public int getBorder_number() {
		return border_number;
	}
	public void setBorder_number(int border_number) {
		this.border_number = border_number;
	}
	public String getBorder_title() {
		return border_title;
	}
	public void setBorder_title(String border_title) {
		this.border_title = border_title;
	}
	public String getBorder_name() {
		return border_name;
	}
	public void setBorder_name(String border_name) {
		this.border_name = border_name;
	}
	public String getCreatedate() {
		return createdate;
	}
	public void setCreatedate(String createdate) {
		this.createdate = createdate;
	}
	public String getUpdatedate() {
		return updatedate;
	}
	public void setUpdatedate(String updatedate) {
		this.updatedate = updatedate;
	}
	
	
	
}
